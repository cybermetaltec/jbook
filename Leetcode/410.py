class Solution:
    def splitArray(self, nums: List[int], m: int) -> int:
        def split(mid):
            count, total = 1, 0
            for n in nums:
                total += n
                if total > mid:
                    count += 1
                    total = n
            return count

        left = right = 0
        for n in nums:
            right += n
            left = max(left, n)
        while left < right:
            mid = (left + right) // 2
            if (s := split(mid)) <= m:
                right = mid
            else:
                left = mid + 1
        return left


